
// PRIMITIVE DATA TYPES
// Number
var number1 = 45;
var number2 = 55;
console.log(number1+number2);

// Strings
var str1 = 'Priyansh';
var str2 = "Varun";
console.log(str1, str2);


// Boolean
var isApproved = true;
var isManaged = false;
console.log(isApproved);
console.log(isManaged);

// REFERENCE DATA TYPES
// Arrays
var arr = [1,2,3,4,5];
console.log(arr);
console.log(arr[0]);


// Object
var objPerson = {
    name : "Priyansh",
    age : 19,
    marks : 90
};
console.log(objPerson);
console.log(objPerson.age);